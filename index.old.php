<section id="why"><h2>Per què?</h2><p>
La nostra missió és impulsar la cultura musical local, millorant la <u>visibilitat i el creixement sostenible</u>. Treballem per una <u>comunicació efectiva</u> entre músics locals, el públic i la comunitat andorrana, abordant la <u>desigualtat d'oportunitats</u> i <u>enriquint l'educació i els espais culturals</u> per als joves. També treballem per a <u>implicar i responsabilitzar a les entitats públiques</u> per la millora del sector.
</p></section>
<section id="future-goals"><h2>Futurs Objectius</h2><p>
Ens proposem col·laborar amb la SDADV per enfortir la cultura musical a Andorra, incloent la possibilitat de tocar en espais públics, professionalització del sector i promoció d'esdeveniments. També estem treballant per al desenvolupament d'un portal d'usuaris per a la millora de la identificació del soci. A més, busquem millorar l'educació musico social mitjançant noves iniciatives. <b>Seguim en recerca de nous col·laboradors</b>, així com amb un exhaustiu estudi del sector. 
</p></section>
