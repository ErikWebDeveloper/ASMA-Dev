    <h2>Presentació</h2>
    <p>L'Associació de Músics d'Andorra està al front d'una <u>era de creixement i professionalització</u>. Amb l'objectiu de <u>superar la falta de recursos i visibilitat</u>, la clau està en la col·laboració amb entitats culturals i governamentals, a més d'invertir en educació musical. Aquestes accions no només <u>enriquiran el teixit cultural andorrà</u> sinó que també <u>oferiran noves oportunitats i inspiració als músics locals</u>, marcant l'inici d'una nova etapa per a la música a Andorra.</p>
    <p></p>
</section>
<section>
    <h2>El Nostre Equip</h2>
    <p>L'Associació està <u>formada per una junta de vuit membres</u> dedicats i apassionats per la música. El nostre equip <u>inclou músics professionals</u> (així com altres medis i entitats personals i professionals) que treballen per millorar la qualitat de vida del sector i promoure la música al país.</p>
</section>
<section>
    <h2>Objectius</h2>
    <p>Els nostres objectius són:</p>
    <ul>
        <li>Promoure i donar suport a la música a Andorra.</li>
        <li>Millorar la comunicació entre la comunitat, la població, les entitats privades i els mitjans de difusió i comunicació.</li>
        <li>Generar una comunitat activa entre els músics.</li>
        <li>Disposar d'una base de dades activa dels músics per a millorar l'accés i la comunicació de la comunitat.</li>
        <li>Oferir oportunitats de desenvolupament i col·laboració als músics locals.</li>
        <li>Millorar el desenvolupament i l'educació cultural dels joves.</li>
        <li>Millorar l'estabilitat del mercat de contractacions.</li>
        <li>Crear mitjans de formació fiables per als músics.</li>
        <li>Millorar la disponibilitat dels bucs d'assaig.</li>
        <li>Crear oportunitats de desenvolupament jurídic per la comunitat.</li>
        <li>Implicar fermament a les entitats polítiques i culturals responsables de la millora del sector.</li>
        <li>Organitzar esdeveniments musicals per a la comunitat.</li>
    </ul>
</section>
